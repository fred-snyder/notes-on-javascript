// ternary operator

let a = 5;
let b = "5";

if (a == b) {
    console.log(a) // 5
} else {
    console.log(b)
}

console.log(a == b ? true : false) // true
console.log(a === b ? true : false) // false

// shorthand syntax for the above if statement
/*
condition ? if true : else
condition ? if true : if not true
condition ? value if true : value if false

*/ 
