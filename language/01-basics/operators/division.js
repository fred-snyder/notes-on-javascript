// number / string
console.log(24 / "Hello") // NaN


// float / float
console.log(1.4 / 2.2) // float


// integer / integer
console.log(12/3) // 4
console.log(11/3) // 3.66666666665
console.log((11/3).toFixed(2)) // 3.67


// divide by zero
console.log(10 / 0) // infinity
console.log(10 / Infinity) // 0
