// shift (remove first)

var arr = [1, 2, 3, 4 ,5]

// shift the first element from the passed array
arr.shift();

console.log(arr) // [2 ,3, 4, 5]

// array.shift() remove the first element from the array
// and returns the removed element
console.log(arr.shift()) // 2
console.log(arr)// [3, 4, 5]
